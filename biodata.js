// 1. Variabel dengan const (nilai tetap, tidak boleh diubah)
const namaLengkap = "Gilang";
const usia = 23;
const kota = "Pekanbaru";

// 2. Variabel dengan let (nilai boleh berubah)
let moodHariIni = "Anjay";

// 3. Tampilkan menggunakan template literal
console.log(`Nama saya ${namaLengkap}, usia ${usia} tahun, tinggal di ${kota}.`);
console.log(`Mood saya hari ini: ${moodHariIni}`);